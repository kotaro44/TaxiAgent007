    private void update(){
        if( this.running ){
            canvas.updateCity();
        }
    }
    
    private void startAnimation(){
       this.running = true;
    }
    
    private void stopAnimation(){
        this.running = false;
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        this.update();
        this.canvas.paint(this.canvas.getGraphics());
    }
    
    private void initAnimation(){
        this.running = false;
        timer = new Timer(100, this);
        timer.start();
    }
    
